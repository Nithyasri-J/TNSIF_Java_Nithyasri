package com.Tnsif.productentity;
public class Product {
	private String pname;
	private String pmemory;
	private int pcost;
	/**
	 * @return the pname
	 */
	public String getPname() {
		return pname;
	}
	/**
	 * @param pname the pname to set
	 */
	public void setPname(String pname) {
		this.pname = pname;
	}
	/**
	 * @return the pmemory
	 */
	public String getPmemory() {
		return pmemory;
	}
	/**
	 * @param pmemory the pmemory to set
	 */
	public void setPmemory(String pmemory) {
		this.pmemory = pmemory;
	}
	/**
	 * @return the pcost
	 */
	public int getPcost() {
		return pcost;
	}
	/**
	 * @param pcost the pcost to set
	 */
	public void setPcost(int pcost) {
		this.pcost = pcost;
	}
}

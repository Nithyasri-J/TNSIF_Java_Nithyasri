package com.Tnsif.Studententity;
public class Student {
	private int Sreg;
	private String Sname;
	private int SScore;
	/**
	 * @return the sreg
	 */
	public int getSreg() {
		return Sreg;
	}
	/**
	 * @param sreg the sreg to set
	 */
	public void setSreg(int sreg) {
		Sreg = sreg;
	}
	/**
	 * @return the sname
	 */
	public String getSname() {
		return Sname;
	}
	/**
	 * @param sname the sname to set
	 */
	public void setSname(String sname) {
		Sname = sname;
	}
	/**
	 * @return the sScore
	 */
	public int getSScore() {
		return SScore;
	}
	/**
	 * @param sScore the sScore to set
	 */
	public void setSScore(int sScore) {
		SScore = sScore;
	}
}
